

a = 11
b = 19

# Phép toán số học
print("a + b =", a + b)
print("a - b =", a - b)
print("a * b =", a * b)
print("a // b =", a // b)
print("a ** b =", a ** b)
print("a % b =", a % b)

# So sánh
print("a > b:", a > b)
print("a < b:", a < b)
print("a == b:", a == b)

# Phép logic
print("a AND b =", a & b)
print("a OR b  =", a | b)
print("a XOR b =", a ^ b)
print("NOT a == b:", not(a == b))

# Dịch bit
print("a dịch phải 5 bit:", a >> 5)

# Dịch trái 6 bit
print("a dịch trái 6 bit:", a << 6 )

# In hệ cơ số 2 đảo ngược của a
print("Hệ cơ số 2 đảo ngược của a:",bin(a)[:1:-1])